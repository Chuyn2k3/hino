class TextConverter {
  // Map Vietnamese characters to ASCII equivalents
  static const Map<String, String> _vietnameseToAscii = {
    // Lowercase vowels
    'à': 'a', 'á': 'a', 'ả': 'a', 'ã': 'a', 'ạ': 'a',
    'ă': 'a', 'ằ': 'a', 'ắ': 'a', 'ẳ': 'a', 'ẵ': 'a', 'ặ': 'a',
    'â': 'a', 'ầ': 'a', 'ấ': 'a', 'ẩ': 'a', 'ẫ': 'a', 'ậ': 'a',
    'è': 'e', 'é': 'e', 'ẻ': 'e', 'ẽ': 'e', 'ẹ': 'e',
    'ê': 'e', 'ề': 'e', 'ế': 'e', 'ể': 'e', 'ễ': 'e', 'ệ': 'e',
    'ì': 'i', 'í': 'i', 'ỉ': 'i', 'ĩ': 'i', 'ị': 'i',
    'ò': 'o', 'ó': 'o', 'ỏ': 'o', 'õ': 'o', 'ọ': 'o',
    'ô': 'o', 'ồ': 'o', 'ố': 'o', 'ổ': 'o', 'ỗ': 'o', 'ộ': 'o',
    'ơ': 'o', 'ờ': 'o', 'ớ': 'o', 'ở': 'o', 'ỡ': 'o', 'ợ': 'o',
    'ù': 'u', 'ú': 'u', 'ủ': 'u', 'ũ': 'u', 'ụ': 'u',
    'ư': 'u', 'ừ': 'u', 'ứ': 'u', 'ử': 'u', 'ữ': 'u', 'ự': 'u',
    'ỳ': 'y', 'ý': 'y', 'ỷ': 'y', 'ỹ': 'y', 'ỵ': 'y',
    'đ': 'd',

    // Uppercase vowels
    'À': 'A', 'Á': 'A', 'Ả': 'A', 'Ã': 'A', 'Ạ': 'A',
    'Ă': 'A', 'Ằ': 'A', 'Ắ': 'A', 'Ẳ': 'A', 'Ẵ': 'A', 'Ặ': 'A',
    'Â': 'A', 'Ầ': 'A', 'Ấ': 'A', 'Ẩ': 'A', 'Ẫ': 'A', 'Ậ': 'A',
    'È': 'E', 'É': 'E', 'Ẻ': 'E', 'Ẽ': 'E', 'Ẹ': 'E',
    'Ê': 'E', 'Ề': 'E', 'Ế': 'E', 'Ể': 'E', 'Ễ': 'E', 'Ệ': 'E',
    'Ì': 'I', 'Í': 'I', 'Ỉ': 'I', 'Ĩ': 'I', 'Ị': 'I',
    'Ò': 'O', 'Ó': 'O', 'Ỏ': 'O', 'Õ': 'O', 'Ọ': 'O',
    'Ô': 'O', 'Ồ': 'O', 'Ố': 'O', 'Ổ': 'O', 'Ỗ': 'O', 'Ộ': 'O',
    'Ơ': 'O', 'Ờ': 'O', 'Ớ': 'O', 'Ở': 'O', 'Ỡ': 'O', 'Ợ': 'O',
    'Ù': 'U', 'Ú': 'U', 'Ủ': 'U', 'Ũ': 'U', 'Ụ': 'U',
    'Ư': 'U', 'Ừ': 'U', 'Ứ': 'U', 'Ử': 'U', 'Ữ': 'U', 'Ự': 'U',
    'Ỳ': 'Y', 'Ý': 'Y', 'Ỷ': 'Y', 'Ỹ': 'Y', 'Ỵ': 'Y',
    'Đ': 'D',
  };

  /// Convert Vietnamese text to ASCII equivalent
  /// Example: "phạm viết chuyên" -> "pham viet chuyen"
  static String toAscii(String input) {
    String result = input;

    // Replace Vietnamese characters with ASCII equivalents
    _vietnameseToAscii.forEach((vietnamese, ascii) {
      result = result.replaceAll(vietnamese, ascii);
    });

    return result;
  }

  /// Convert to ASCII and make lowercase for consistent formatting
  static String toAsciiLowerCase(String input) {
    return toAscii(input).toLowerCase();
  }

  /// Convert to ASCII and format for NFC (uppercase, no spaces, no special characters)
  static String toAsciiForNfc(String input) {
    return toAscii(input)
        .toUpperCase()
        .replaceAll(RegExp(r'[^A-Z0-9 ]'), ''); // CHO PHÉP SPACE
  }

  /// Convert Vietnamese text to clean uppercase ASCII for NFC writing
  /// Example: "phạm viết chuyên!" -> "PHAMVIETCHUYEN"
  static String toNfcFormat(String input) {
    String result = input;

    // Replace Vietnamese characters with ASCII equivalents
    _vietnameseToAscii.forEach((vietnamese, ascii) {
      result = result.replaceAll(vietnamese, ascii);
    });

    // Convert to uppercase and remove all non-alphanumeric characters
    return result.toUpperCase().replaceAll(RegExp(r'[^A-Z0-9]'), '');
  }

  /// Check if string contains only ASCII characters
  static bool isAscii(String str) => str.codeUnits.every((c) => c <= 127);
}

extension NormalizeText on String {
  String normalize() {
    return this
        .replaceAll(RegExp(r'\s+'), ' ') // chuẩn hóa space
        .trim();
  }
}
